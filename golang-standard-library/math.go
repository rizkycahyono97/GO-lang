package main

import (
	"fmt"
	"math"
)

func main() {
	fmt.Println(math.Ceil(1.40))	// membulatkan keatas
	fmt.Println(math.Floor(1.40))	// membulatkan kebawah
	fmt.Println(math.Round(1.40))	// membulatkan paling dekat
	fmt.Println(math.Max(10, 11))	// angka paling max
	fmt.Println(math.Max(10, 11))	// angka paling Min

}