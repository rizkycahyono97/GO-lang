package main

import "fmt"

func main() {
	fmt.Println("Rizky")
	fmt.Println("Rizky Cahyono")
	fmt.Println("Rizky Cahyono Putra")

	fmt.Println(len("Rizky")) // untuk menghitung jumlah karakter string
	fmt.Println("Rizky Cahyono"[0])	// posisi karakter pada string dengan output bite
	fmt.Println("Rizky Cahyono Putra"[1])
}